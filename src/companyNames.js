/**
 * Возвращает человекочитаемое название клиента для company_id.
 * Если company_id неизвестен — возвращает плейсхолдер вида
 * "Неизвестно (id=XXXX)", чтобы это было заметно в таблице
 * и можно было опознать и дописать в словарь вручную.
 */
function resolveCompanyName(companyId) {
  if (companyId == null) return 'Неизвестно (без id)';
  return COMPANY_NAMES[companyId] || `Неизвестно (id=${companyId})`;
}

